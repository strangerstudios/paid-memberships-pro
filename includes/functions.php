	$pmpro_price_parts_with_total = apply_filters( 'pmpro_get_price_parts_with_total', $pmpro_price_parts_with_total, $pmpro_invoice );

	if ( $format == 'array' ) {
		return $pmpro_price_parts_with_total;
	} else {
